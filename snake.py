def нарисовать_объект(объект, цвет):
    x, y = объект[0] * РАЗМЕР_КЛЕТКИ, объект[1] * РАЗМЕР_КЛЕТКИ
    pygame.draw.rect(экран, цвет, (x, y, РАЗМЕР_КЛЕТКИ, РАЗМЕР_КЛЕТКИ))

