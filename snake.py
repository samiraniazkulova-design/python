import array
def бош_ун_жарат(frequency=440, duration=0.1):
    sample_rate = 22050
    n_samples = int(sample_rate * duration)
    buf = array.array('h', [int(32767 * 0.5 * ( 1 if (i // (sample_rate // (2 * frequency))) % 2 == 0 else -1)) for i in range(n_samples)])
    return pygame.mixer.Sound(buffer=buf)

