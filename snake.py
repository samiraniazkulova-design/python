# --- Логика игры ---
def перезапустить_игру():
    global змейка, направление, очки, игра_завершена, яблоко
    змейка = [[10, 10]]
    направление = (1, 0)
    очки = 0
    игра_завершена = False
    яблоко = сгенерировать_яблоко()

def обновить_игру():
    global змейка, направление, очки, игра_завершена, яблоко
    
    голова_x, голова_y = змейка[0]
    новая_голова = [голова_x + направление[0], голова_y + направление[1]]
    
    # Проверка столкновения
    if not (0 <= новая_голова[0] < ШИРИНА // РАЗМЕР_КЛЕТКИ and 0 <= новая_голова[1] < ВЫСОТА // РАЗМЕР_КЛЕТКИ) or новая_голова in змейка[1:]:
        игра_завершена = True
        return

    змейка.insert(0, новая_голова)

    # Съедание яблока
    if новая_голова == яблоко:
        очки += 1
        яблоко = сгенерировать_яблоко()
    else:
        змейка.pop()

# --- Главный игровой цикл ---
работает = True
while работает:
    for событие in pygame.event.get():
        if событие.type == pygame.QUIT:
            работает = False
        
        # Обработка движения змейки по таймеру
        if событие.type == pygame.USEREVENT and not игра_завершена:
            обновить_игру()
            
        # Обработка ввода с клавиатуры
        if событие.type == pygame.KEYDOWN:
            if событие.key == pygame.K_UP and направление != (0, 1):
                направление = (0, -1)
            elif событие.key == pygame.K_DOWN and направление != (0, -1):
                направление = (0, 1)
            elif событие.key == pygame.K_LEFT and направление != (1, 0):
                направление = (-1, 0)
            elif событие.key == pygame.K_RIGHT and направление != (-1, 0):
                направление = (1, 0)
            elif событие.key == pygame.K_r and игра_завершена:
                перезапустить_игру()

    нарисовать_игру()
    часы.tick(30) # Ограничиваем FPS для отрисовки

pygame.quit()
