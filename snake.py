 # Функция для генерации яблока
def сгенерировать_яблоко():
    while True:
        x = random.randrange(0, ШИРИНА // РАЗМЕР_КЛЕТКИ)
        y = random.randrange(0, ВЫСОТА // РАЗМЕР_КЛЕТКИ)
        if [x, y] not in змейка:
            return [x, y]

яблоко = сгенерировать_яблоко()
